# Works With Strings
for letter in "ABCDEFG":
  print(letter)

# With Lists
for item in [1, 2, 3, 4, 5]:
  print(item)

# With Sets
for item in {1, 2, 3, 4, 5}:
  print(item)

# With Tuples
for item in (1, 2, 3, 4, 5):
  print(item)

# Nested Loops

for item in (1, 2, 3, 4, 5):
  for letter in ['a', 'b', 'c']:
    print(item, letter)